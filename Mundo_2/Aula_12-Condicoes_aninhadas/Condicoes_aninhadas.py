a = 2

if a < 3:
    print('o valor é men que 3')
# Utilizando o elif é economizado tempo que o progama roda, fazendo com que a resposta seja mais rápida e que
# não haja repetições que façam ter um pergunta tenha mais de uma resposta.
elif a > 3:
    print('o valor é mai que 3')
elif a == 3:
    print('o valor é igual a 3')